let maskV2: Uint8Array | null = null;
let maskLoadPromise: Promise<void> | null = null;

function getMask(pos: number): number {
  if (!maskV2?.length) throw new Error("KGM 解密数据未加载");
  return MASK_V2_PREDEF[pos % 272]! ^ maskV2[pos >> 4]!;
}

export async function loadKgmMask(): Promise<void> {
  if (maskV2?.length) return;
  if (maskLoadPromise) return maskLoadPromise;

  maskLoadPromise = (async () => {
    const res = await fetch("/static/kgm.mask");
    if (!res.ok) {
      throw new Error(
        "缺少酷狗解密数据 (kgm.mask)。请运行 ./scripts/download-kgm-mask.sh 后刷新页面",
      );
    }
    maskV2 = new Uint8Array(await res.arrayBuffer());
    if (maskV2.length < 4096) {
      maskV2 = null;
      throw new Error("kgm.mask 文件无效，请重新下载");
    }
  })();

  try {
    await maskLoadPromise;
  } finally {
    maskLoadPromise = null;
  }
}

export async function decryptKgm(file: File, ext: "kgm" | "vpr"): Promise<UnlockResult> {
  await loadKgmMask();

  const oriData = new Uint8Array(await readArrayBuffer(file));
  if (ext === "vpr") {
    if (!bytesHasPrefix(oriData, VPR_HEADER)) throw new Error("不是有效的 VPR 文件");
  } else if (!bytesHasPrefix(oriData, KGM_HEADER)) {
    throw new Error("不是有效的 KGM 文件");
  }

  const headerLen = new DataView(oriData.slice(0x10, 0x14).buffer).getUint32(0, true);
  const audioData = oriData.slice(headerLen);
  if (audioData.byteLength > 1 << 26) {
    throw new Error("文件过大（>64MB），请分批转换");
  }

  const key1 = new Uint8Array(17);
  key1.set(oriData.slice(0x1c, 0x2c), 0);

  const dataLen = audioData.length;
  for (let i = 0; i < dataLen; i++) {
    let med8 = key1[i % 17]! ^ audioData[i]!;
    med8 ^= (med8 & 0xf) << 4;
    let msk8 = getMask(i);
    msk8 ^= (msk8 & 0xf) << 4;
    audioData[i] = med8 ^ msk8;
  }

  if (ext === "vpr") {
    for (let i = 0; i < dataLen; i++) {
      audioData[i]! ^= VPR_MASK_DIFF[i % 17]!;
    }
  }

  const outExt = sniffAudioExt(audioData);
  const mime = AudioMimeType[outExt] ?? "application/octet-stream";
  const { title, artist } = getMetaFromFilename(file.name);
  const blob = new Blob([audioData], { type: mime });

  return {
    blob,
    filename: safeDownloadName(title, artist, outExt),
    title,
    artist,
    ext: outExt,
    mime,
  };
}
